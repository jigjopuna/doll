&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ชาวบ้านที่นี่ส่วนใหญ่ประกอบอาชีพ เกษตรกรรม การปศุสัตว์ และอุตสาหกรรมการดูดทราย เนื่องจากสภาพพื้นดินเอื้ออำนวยทำให้ อำเภอป่าโมกเป็นที่รู้จักทั่วไปในฐานะแหล่งส่งออกทรายน้ำจืดใหญ่เป็นอันดับต้นๆ ของประเทศ<br><br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ณ หมู่ที่ ๑ ตำบลบางเสด็จ อำเภอป่าโมก เป็นที่ตั้งกลุ่มอาชีพ ปั้นตุ๊กตาชาววัง เมื่อย้อนเวลากลับไปปลายปี พุทธศักราช ๒๕๑๘ ที่นี่เกิดอุทกภัยอย่างแสนสาหัส ไร่นาจมอยู่ใต้พื้นน้ำที่ไหล่บ่า เออล้นตลิ่งจากแม่น้ำเจ้าพระยาเข้าท่วมท้องทุ่งนาทั้งที่ข้าวชูร่วงรอการเก็บเกี่ยว ชาวบ้านบางเสด็จต้องลำบากยากเข็ญปางเลือดตากระเด็น ต้องไปปั้นอิฐและเหลาก้านธูปขายเพื่อประทังชีวิตต้องอดอยากยากแค้นในปีนั้น<br><br>

	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;เปรียบดั่งฟ้าหลงฝน เมื่อ พุทธศักราช ๒๕๑๙ สมเด็จพระนางเจ้าสิริกิตติ์พระบรมราชินีนาถได้เสด็จพระราชดำเนินเยี่ยมราษฎร อำเภอป่าโมก ทรงทราบปัญหาน้ำท่วมท้องนาของราษฎรเสียหายติดต่อกันหลายปีทำนาไม่ได้ผล ราษฎรมีฐานะยากจนเพิ่มขึ้น จึงพระราชดำริที่จะหาอาชีพเสริมให้ทรงเล็งเห็นว่าตุ๊กตาชาววังแบบโบราณซึ่งทำด้วยดินปั้นเป็นรูปจำลองลักษณะท่าทาง และ อิริยาบถ ต่างๆ ที่อยู่ปกติตามชีวิตประจำวันของคนไทย ซึ่งหาดูได้ยากและเกือบจะสูญสิ้นไป ประกอบกับราษฎรในบริเวณนี้มีอาชีพทำอิฐอยู่แล้ว คงจะหาวัตถุดิบคือดินเหนียวที่จะนำมาปั้นตุ๊กตาดินได้ไม่ยากนัก จึงมีพระกรุณาโปรดเกล้าโปรดกระหม่อม ให้จัดตั้งโครงการปั้นตุ๊กตาชาววังขึ้นเมื่อเดือน มกราคม พุทธศักราช ๒๕๑๙ เมื่อเสร็จสิ้นทรงโปรดเกล้าโปรดกระหม่อมให้สมเด็จพระเทพ เสด็จมาเป็นองค์ประธานในพิธีไหว้ครู ซึ่งสมเด็จพระนางเจ้าสิริกิตติ์พระบรมราชินีนาถได้ให้อาจารย์จุลทรรศน์ พยาฆรานนท์ จากวิทยาลับเพาะช่างมาสอน มีชาวบ้านเข้ารับการอบรมประมาณ ๑๕๑ คนเป็นระยะเวลา ๓ เดือน ณ ศาลาวัดท่าสุทาวาส หลังจากนั้น อาจารย์ไม่ได้ทิ้งให้ชาวบ้านอยู่ลำพัง อาจารย์กลับมาให้คำปรึกษาอยู่เป็นระยะๆ สร้างความผูกพันธ์กันเป็นอย่างมาก เมื่อปั้นตุ๊กตาได้แล้วผลงานในระยะแรกพระราชินีทรงรับซื้อไว้ทั้งหมดเพื่อเป็นขวัญและกำลังใจของราษฎร พร้อมทั้งหาตลาดให้ ต่อมาตุ๊กตาชาววังรู้จักกันอย่างแพร่หลาย ราษฎรผู้ผลิตก็หาตลาดได้เองโดยมีผู้สนใจหาซื้อไปติดต่อถึงหมู่บ้าน<br><br>

	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ตุ๊กตาชาววังเป็นตุ๊กตาไทยชนิดหนึ่งทำด้วยดินปั้นเป็นรูปจำลองและย่อส่วน ตุ๊กตาชนิดนี้มีขนาดเล็กมากท่าปั้นและลักษณะทรวดทรง เลียบแบบชีวิตประจำวันของคนไทย มีความน่ารักน่าเอ็นดูการตกแต่งหน้าตาที่สวยงามประกอบกับสีสันสดใส ทำให้ตุ๊กตาไทยชนิดนี้ มีความสวยงามเป็นเสน่ห์มีคุณค่าทางจิตใจของชาวบ้าน<br><br>
	
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;อาจารย์จุลทรรศน์ พยาฆรานนท์ ให้ความเห็นว่า ตุ๊กตาชาววังน่าจะเกิดขึ้นครั้งแรกในพระบรมมหาราชวัง ตุ๊กตาชาววังในระยะแรก ทำเป็นของเล่นซึ่งมีมาก่อน สมัยพระบาทสมเด็จพระจุลจอมเกล้าเจ้าอยู่หัว รัชกาลที่ ๕ โดยอาศัยจากการสันนิฐานจากทรงผมของตุ๊กตา ทั้งผู้หญิงและผู้ชาย ซึ่งทำผมและระบายสีเป็นผมทรงดอกกระทุ่ม หรือรองทรง และทรงผมทรงโปร่งเหมือนทรงผมของผู้หญิงญี่ปุ่น ที่มีมานิยมในภายหลัง ความนิยมเล่นตุ๊กตาชาววังมีอยู่ในวงจำกัด ไม่แพร่หลายออกมาสู่คนทั่วไป ซึ่งอยู่ภายนอกพระบรมมหาราชวัง เมื่อสิ้นรัชสมัยรัชกาลที่ ๕ มีคุณข้าหลวงบางท่านมาอาศัยอยู่กับครอบครัวนอกวังจึงได้ปั้นตุ๊กตาออกจำหน่าย ในระยะแรกไม่เป็นที่รู้จักมากนัก ต่อมาเมื่อได้นำไปฝากขายในงานประจำปีต่างๆ เช่นวัดสระเกตุ หรืองานไหว้พระภูเขาทองโดยทำเป็นรูปผู้หญิง ผู้ชาย เด็ก ทำอิริยาบถลอกเลียนกริยาท่าทางของคนไทย ตุ๊กตาเหล่านี้ถูกปั้นขึ้นเป็นตัวจัดเป็นสำหรับชุดต่างๆ แสดงความเป็นอยู่ของคนไทยแต่ก่อน เช่น ร่วมวงสนทนา การเล่นดนตรี การร่วมวงรับประทานอาหาร เป็นต้น จึงมีคนรู้จักแพร่หลายออกไป นอกจากตุ๊กตาชาววังยังมีคุณค่าที่แฝงอยู่ในรูปวัตถุที่แสดงให้ประจักษ์ของวัฒนธรรมไทย ด้านความเป็นอยู่ การแต่ง ประเพณี และการละเล่นพื้นบ้าน

						   
